// Use var to DECLARE three variables. The variables should have package level scope.
// Do not assign VALUES to the variables.

package main

import (
	"fmt"
)

var x int
var y string
var z bool

func main() {

	//Print out the values for each identifier
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)

}
